#include "libft.h"

void	ft_bzero(void *s, int nbyte)
{
	int		i;
	char	*str;

	str = (char *)s;
	i = 0;
	if (nbyte <= 0)
		return ;
	while (i < nbyte)
	{
		str[i] = 0;
		i++;
	}
}
